namespace PasPasPas.Api.Options {

    /// <summary>
    ///     switch for design-time only units
    /// </summary>
    public enum DesignOnlyUnit {

        /// <summary>
        ///     undefined status
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     unit design time only
        /// </summary>
        InDesignTimeOnly = 1,

        /// <summary>
        ///     allow unit always
        /// </summary>
        Alltimes = 2,
    }
}