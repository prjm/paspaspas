namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag to enable long delphi strings
    /// </summary>
    public enum LongDelphiStrings {

        /// <summary>
        ///     undefined string status
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable long strings
        /// </summary>
        EnableLongStrings = 1,

        /// <summary>
        ///     disable long strings
        /// </summary>
        DisableLongStrings = 2,
    }
}
