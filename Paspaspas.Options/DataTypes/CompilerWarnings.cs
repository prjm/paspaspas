namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag to enable / disable compiler warnings
    /// </summary>
    public enum CompilerWarnings {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     disable warnings
        /// </summary>
        Disable = 1,

        /// <summary>
        ///     enable warnings
        /// </summary>
        Enable = 2,
    }
}