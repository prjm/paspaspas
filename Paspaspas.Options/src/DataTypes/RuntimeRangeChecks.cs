namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag to generate runtime range checks
    /// </summary>
    public enum RuntimeRangeChecks {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable range checks
        /// </summary>
        EnableRangeChecks = 1,

        /// <summary>
        ///     disable range checks
        /// </summary>
        DisableRangeChecks = 2,
    }
}