namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     switch to enable extended syntax
    /// </summary>
    public enum ExtendedSyntax {

        /// <summary>
        ///     flag undefined
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     disable extended syntax
        /// </summary>
        NoExtendedSyntax = 1,

        /// <summary>
        ///     enable extended syntax
        /// </summary>
        UseExtendedSyntax = 2,
    }
}