namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag for stack frame generation
    /// </summary>
    public enum StackFrameGeneration {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable all stack frames
        /// </summary>
        EnableFrames = 1,

        /// <summary>
        ///     disable all stack frames
        /// </summary>
        DisableFrames = 2,
    }
}