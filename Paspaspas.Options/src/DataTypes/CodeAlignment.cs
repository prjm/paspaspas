namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     Option for code alignment
    /// </summary>
    public enum CodeAlignment {

        /// <summary>
        ///     Undefined alignment
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     $CODEALIGN 1
        /// </summary>
        OneByte = 1,

        /// <summary>
        ///     $CODEALIGN 2
        /// </summary>
        TwoByte = 2,


        /// <summary>
        ///     $CODEALIGN 4
        /// </summary>
        FourByte = 4,


        /// <summary>
        ///     $CODEALIGN 8
        /// </summary>
        EightByte = 8,


        /// <summary>
        ///     $CODEALIGN 16
        /// </summary>
        SixteenByte = 16

    }
}
