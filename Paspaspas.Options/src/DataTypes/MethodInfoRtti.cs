namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag to enable method info rtti
    /// </summary>
    public enum MethodInfoRtti {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable method info
        /// </summary>
        EnableMethodInfo = 1,

        /// <summary>
        ///     disable method info
        /// </summary>
        DisableMethodInfo = 2,
    }
}