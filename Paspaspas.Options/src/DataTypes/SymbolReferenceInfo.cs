namespace PasPasPas.Options.DataTypes {

    /// <summary>
    ///     flag to generate reference information
    /// </summary>
    public enum SymbolReferenceInfo {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable symbol reference info
        /// </summary>
        Enable = 1,

        /// <summary>
        ///     disable symbol reference info
        /// </summary>
        Disable = 2,
    }
}