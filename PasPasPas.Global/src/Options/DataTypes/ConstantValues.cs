namespace PasPasPas.Globals.Options.DataTypes {

    /// <summary>
    ///     flag to enable writable constants
    /// </summary>
    public enum ConstantValue {

        /// <summary>
        ///     undefined flag state
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     constant constants
        /// </summary>
        Constant = 1,

        /// <summary>
        ///     writable constants
        /// </summary>
        Writable = 2,
    }

}