namespace PasPasPas.Globals.Options.DataTypes {

    /// <summary>
    ///     flag to enable long strings
    /// </summary>
    public enum LongStringMode {

        /// <summary>
        ///     undefined string status
        /// </summary>
        Undefined = 0,

        /// <summary>
        ///     enable long strings
        /// </summary>
        EnableLongStrings = 1,

        /// <summary>
        ///     disable long strings
        /// </summary>
        DisableLongStrings = 2,
    }
}
